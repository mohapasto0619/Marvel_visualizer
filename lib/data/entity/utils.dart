enum Extension { JPG }

class EnumValues<T> {
  Map<String, T> map;
  Map<T, String>? reverseMap;

  EnumValues(this.map);

  Map<T, String>? get reverse {
    reverseMap ??= map.map((k, v) => MapEntry(v, k));
    return reverseMap;
  }
}

final extensionValues = EnumValues({"jpg": Extension.JPG});

enum UrlType { DETAIL, WIKI, COMICLINK }

final urlTypeValues = EnumValues({
